class TeamGrantController extends Controller{
    private $items = 40; 
    public function index(Request $request){
        $q = str_replace(' ','%',$request->q);
        $result = WhTeamGrant::where('name','LIKE',"{$q}%")
            ->paginate($this->items); 
        $result->appends(['q' => $request->q]);
        return view('master.teamgrant',[
            'result' => $result,
            'q' => $request->q,
        ]);
    }

